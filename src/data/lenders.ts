// Helper function to group lenders by first letter
export function groupLendersByLetter(lendersList: Lender[]): Record<string, Lender[]> {
  return lendersList.reduce((acc, lender) => {
    const firstLetter = lender.name.charAt(0).toUpperCase();
    if (!acc[firstLetter]) {
      acc[firstLetter] = [];
    }
    acc[firstLetter].push(lender);
    return acc;
  }, {} as Record<string, Lender[]>);
}

// Get all available letters
export function getAvailableLetters(lendersList: Lender[]): string[] {
  const letters = [...new Set(lendersList.map(l => l.name.charAt(0).toUpperCase()))];
  return letters.sort();
}

// Get lender by slug
export function getLenderBySlug(slug: string): Lender | undefined {
  return lenders.find(l => l.slug === slug);
}
